#include<iostream>
using namespace std;
int main(){
    int phy=25;
    int che=29;
    int math=20;
    int eng=35;
    int phe=30;
    cout<<"phy="<<phy<<endl;
    cout<<"che="<<che<<endl;
    cout<<"math="<<math<<endl;
    cout<<"eng="<<eng<<endl;
    cout<<"phe="<<phe<<endl;
    cout<<"addition="<<(phy+che+math+eng+phe)<<endl;
    cout<<"average="<<((phy+che+math+eng+phe)/5)<<endl;
    return 0;
}